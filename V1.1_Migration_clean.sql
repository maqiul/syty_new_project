-- ============================================================
-- 模块二：库存管理闭环（进销存）
-- ============================================================

-- ----------------------------------------
-- 2.1 stock_log 库存流水表
-- ----------------------------------------
CREATE TABLE IF NOT EXISTS stock_log (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id       BIGINT       NOT NULL,
    shop_id         BIGINT       NOT NULL,
    string_id       BIGINT       NOT NULL,

    -- 变动信息
    change_type     VARCHAR(20)  NOT NULL,
    quantity        INTEGER      NOT NULL,
    before_quantity INTEGER      NOT NULL,
    after_quantity  INTEGER      NOT NULL,

    -- 关联业务
    order_id        BIGINT       DEFAULT NULL,
    order_no        VARCHAR(50)  DEFAULT '',

    -- 操作信息
    operator_id     BIGINT       DEFAULT NULL,
    operator_name   VARCHAR(50)  DEFAULT '',
    remark          VARCHAR(200) DEFAULT '',

    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE stock_log IS '库存流水表';
CREATE INDEX idx_stock_log_shop ON stock_log(shop_id);
CREATE INDEX idx_stock_log_string ON stock_log(string_id);
CREATE INDEX idx_stock_log_order ON stock_log(order_id);
CREATE INDEX idx_stock_log_created ON stock_log(created_at);

-- ----------------------------------------
-- 2.2 shop_string 表增加审计字段
-- ----------------------------------------
ALTER TABLE shop_string ADD COLUMN IF NOT EXISTS updated_by BIGINT DEFAULT NULL;

-- ============================================================
-- 模块三：财务体系搭建（算清账）
-- ============================================================

-- ----------------------------------------
-- 3.1 payment_record 支付流水表
-- ----------------------------------------
CREATE TABLE IF NOT EXISTS payment_record (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id       BIGINT       NOT NULL,
    order_id        BIGINT       NOT NULL,
    order_no        VARCHAR(50)  NOT NULL,
    player_id       BIGINT       DEFAULT NULL,
    player_name     VARCHAR(50)  DEFAULT '',

    -- 金额
    amount          DECIMAL(10,2) NOT NULL,
    pay_method      VARCHAR(20)  NOT NULL,

    -- 支付后状态
    paid_amount_after DECIMAL(10,2) NOT NULL,
    pay_status_after  VARCHAR(20) NOT NULL,

    -- 操作信息
    operator_id     BIGINT       DEFAULT NULL,
    operator_name   VARCHAR(50)  DEFAULT '',
    remark          VARCHAR(200) DEFAULT '',

    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE payment_record IS '支付流水表';
CREATE INDEX idx_payment_order ON payment_record(order_id);
CREATE INDEX idx_payment_player ON payment_record(player_id);
CREATE INDEX idx_payment_method ON payment_record(pay_method);
CREATE INDEX idx_payment_created ON payment_record(created_at);

-- ============================================================
-- 模块四：穿线师绩效（算提成）
-- ============================================================

-- ----------------------------------------
-- 4.1 commission_rule 提成规则配置表
-- ----------------------------------------
CREATE TABLE IF NOT EXISTS commission_rule (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id       BIGINT       NOT NULL,
    shop_id         BIGINT       DEFAULT NULL,
    stringer_id     BIGINT       DEFAULT NULL,

    -- 规则类型
    rule_type       VARCHAR(20)  NOT NULL DEFAULT 'FIXED',

    -- 固定金额规则
    fixed_amount    DECIMAL(10,2) DEFAULT 0,

    -- 百分比规则
    percent_rate    DECIMAL(5,2)  DEFAULT 0,
    percent_base    VARCHAR(20)  DEFAULT 'LABOR',

    -- 阶梯规则 (JSON)
    tier_config     JSONB        DEFAULT NULL,

    -- 生效时间
    effective_from  DATE         NOT NULL DEFAULT CURRENT_DATE,
    effective_to    DATE         DEFAULT NULL,

    enabled         SMALLINT     NOT NULL DEFAULT 1,
    remark          VARCHAR(200) DEFAULT '',
    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE commission_rule IS '提成规则配置表';

