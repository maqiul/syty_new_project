-- ============================================================
-- 模块三：财务体系搭建（算清账）
-- ============================================================

-- ----------------------------------------
-- 3.1 payment_record 支付流水表
-- ----------------------------------------
CREATE TABLE IF NOT EXISTS payment_record (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id       BIGINT       NOT NULL COMMENT '租户ID',
    order_id        BIGINT       NOT NULL COMMENT '关联订单ID',
    order_no        VARCHAR(50)  NOT NULL COMMENT '订单号',
    player_id       BIGINT       DEFAULT NULL COMMENT '客户ID',
    player_name     VARCHAR(50)  DEFAULT '' COMMENT '客户姓名',

    -- 金额
    amount          DECIMAL(10,2) NOT NULL COMMENT '本次支付金额',
    pay_method      VARCHAR(20)  NOT NULL COMMENT '支付方式: CASH/WECHAT/ALIPAY/BANK/CREDIT',

    -- 支付后状态
    paid_amount_after DECIMAL(10,2) NOT NULL COMMENT '支付后该订单累计已付金额',
    pay_status_after  VARCHAR(20) NOT NULL COMMENT '支付后状态',

    -- 操作信息
    operator_id     BIGINT       DEFAULT NULL COMMENT '收款人ID',
    operator_name   VARCHAR(50)  DEFAULT '' COMMENT '收款人姓名',
    remark          VARCHAR(200) DEFAULT '' COMMENT '备注',

    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE payment_record IS '支付流水表';
CREATE INDEX idx_payment_order ON payment_record(order_id);
CREATE INDEX idx_payment_player ON payment_record(player_id);
CREATE INDEX idx_payment_method ON payment_record(pay_method);
CREATE INDEX idx_payment_created ON payment_record(created_at);

-- ============================================================
-- 模块四：穿线师绩效（算提成）
-- ============================================================

-- ----------------------------------------
-- 4.1 commission_rule 提成规则配置表
-- ----------------------------------------
CREATE TABLE IF NOT EXISTS commission_rule (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id       BIGINT       NOT NULL COMMENT '租户ID',
    shop_id         BIGINT       DEFAULT NULL COMMENT '店铺ID(NULL=全局规则)',
    stringer_id     BIGINT       DEFAULT NULL COMMENT '穿线师ID(NULL=默认规则)',

    -- 规则类型
    rule_type       VARCHAR(20)  NOT NULL DEFAULT 'FIXED' COMMENT '规则类型: FIXED-固定金额 PERCENT-百分比 TIERED-阶梯',

    -- 固定金额规则
    fixed_amount    DECIMAL(10,2) DEFAULT 0 COMMENT '固定提成金额(元/支)',

    -- 百分比规则
    percent_rate    DECIMAL(5,2)  DEFAULT 0 COMMENT '提成比例(%, 如 15.00 表示15%)',
    percent_base    VARCHAR(20)  DEFAULT 'LABOR' COMMENT '提成基数: LABOR-手工费 TOTAL-总价 STRING-线材费',

    -- 阶梯规则 (JSON)
    tier_config     JSONB        DEFAULT NULL COMMENT '阶梯配置: [{"min":0,"max":50,"amount":10},{"min":50,"max":null,"amount":15}]',

    -- 生效时间
    effective_from  DATE         NOT NULL DEFAULT CURRENT_DATE,
    effective_to    DATE         DEFAULT NULL COMMENT 'NULL=永久有效',

    enabled         SMALLINT     NOT NULL DEFAULT 1,
    remark          VARCHAR(200) DEFAULT '',
    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE commission_rule IS '提成规则配置表';

