-- ----------------------------------------
-- 2.1 stock_log 库存流水表
-- ----------------------------------------
CREATE TABLE IF NOT EXISTS stock_log (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id       BIGINT       NOT NULL COMMENT '租户ID',
    shop_id         BIGINT       NOT NULL COMMENT '店铺ID',
    string_id       BIGINT       NOT NULL COMMENT '线材ID',

    -- 变动信息
    change_type     VARCHAR(20)  NOT NULL COMMENT '变动类型: PURCHASE_IN-采购入库 ORDER_OUT-订单扣减 MANUAL_ADJUST-手动调整 RETURN_IN-退货入库 TRANSFER-调拨',
    quantity        INTEGER      NOT NULL COMMENT '变动数量(正数为入库,负数为出库)',
    before_quantity INTEGER      NOT NULL COMMENT '变动前库存',
    after_quantity  INTEGER      NOT NULL COMMENT '变动后库存',

    -- 关联业务
    order_id        BIGINT       DEFAULT NULL COMMENT '关联订单ID(订单扣减时填写)',
    order_no        VARCHAR(50)  DEFAULT '' COMMENT '关联订单号',

    -- 操作信息
    operator_id     BIGINT       DEFAULT NULL COMMENT '操作人ID',
    operator_name   VARCHAR(50)  DEFAULT '' COMMENT '操作人姓名',
    remark          VARCHAR(200) DEFAULT '' COMMENT '备注(如采购单号、调整原因)',

    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE stock_log IS '库存流水表';
CREATE INDEX idx_stock_log_shop ON stock_log(shop_id);
CREATE INDEX idx_stock_log_string ON stock_log(string_id);
CREATE INDEX idx_stock_log_order ON stock_log(order_id);
CREATE INDEX idx_stock_log_created ON stock_log(created_at);

