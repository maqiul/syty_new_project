-- ==================== 租户 ====================
CREATE TABLE IF NOT EXISTS tenant (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(200) NOT NULL,
    code            VARCHAR(50)  NOT NULL UNIQUE,
    contact_person  VARCHAR(50)  DEFAULT '',
    phone           VARCHAR(20)  DEFAULT '',
    status          SMALLINT     NOT NULL DEFAULT 1,
    expired_at      TIMESTAMP    DEFAULT NULL,
    remark          VARCHAR(500) DEFAULT '',
    deleted         SMALLINT     NOT NULL DEFAULT 0,
    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE tenant IS '租户';
COMMENT ON COLUMN tenant.name IS '租户名称';
COMMENT ON COLUMN tenant.code IS '租户编码(唯一)';
COMMENT ON COLUMN tenant.status IS '状态: 1-启用 0-禁用';
CREATE INDEX idx_tenant_code ON tenant(code);

-- ==================== 用户 ====================
CREATE TABLE IF NOT EXISTS sys_user (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id   BIGINT       DEFAULT NULL,
    username    VARCHAR(50)  NOT NULL,
    password    VARCHAR(255) NOT NULL,
    real_name   VARCHAR(50)  DEFAULT '',
    phone       VARCHAR(20)  DEFAULT '',
    role        VARCHAR(30)  NOT NULL DEFAULT 'STAFF',
    status      SMALLINT     NOT NULL DEFAULT 1,
    deleted     SMALLINT     NOT NULL DEFAULT 0,
    created_at  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_tenant_username UNIQUE (tenant_id, username)
);
COMMENT ON TABLE sys_user IS '系统用户';
COMMENT ON COLUMN sys_user.tenant_id IS '所属租户ID(SUPER_ADMIN为NULL)';
COMMENT ON COLUMN sys_user.role IS '角色: SUPER_ADMIN-超管 TENANT_ADMIN-租户管理员 STAFF-店员';
COMMENT ON COLUMN sys_user.status IS '状态: 1-启用 0-禁用';
CREATE INDEX idx_sys_user_tenant ON sys_user(tenant_id);

-- 用户-店铺关联
CREATE TABLE IF NOT EXISTS sys_user_shop (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT       NOT NULL,
    shop_id     BIGINT       NOT NULL,
    created_at  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_user_shop UNIQUE (user_id, shop_id)
