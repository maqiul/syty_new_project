-- ==================== RBAC 菜单+权限系统（数据库驱动） ====================

-- 1. 菜单表（树形结构，前端从 API 拉取渲染）
CREATE TABLE IF NOT EXISTS sys_menu (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parent_id       BIGINT       NOT NULL DEFAULT 0,
    name            VARCHAR(100) NOT NULL,
    path            VARCHAR(200) DEFAULT '',
    component       VARCHAR(200) DEFAULT '',
    icon            VARCHAR(50)  DEFAULT '',
    permission_code VARCHAR(100) DEFAULT '',   -- 关联 sys_permission.code
    sort_order      INT          NOT NULL DEFAULT 0,
    type            SMALLINT     NOT NULL DEFAULT 1,   -- 0=目录 1=菜单
    hidden          SMALLINT     NOT NULL DEFAULT 0,
    tenant_id       BIGINT       DEFAULT NULL,          -- NULL=全局菜单
    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE sys_menu IS '系统菜单(树形)';
COMMENT ON COLUMN sys_menu.parent_id IS '父菜单ID, 0=根目录';
COMMENT ON COLUMN sys_menu.type IS '0=目录(展开) 1=菜单(路由)';
COMMENT ON COLUMN sys_menu.hidden IS '1=隐藏(不在侧边栏显示)';
COMMENT ON COLUMN sys_menu.tenant_id IS 'NULL=全局菜单, 非NULL=租户定制菜单';
CREATE INDEX idx_sys_menu_parent ON sys_menu(parent_id);

-- 2. 权限项表
CREATE TABLE IF NOT EXISTS sys_permission (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code        VARCHAR(100) NOT NULL UNIQUE,
    name        VARCHAR(100) NOT NULL,
    menu_id     BIGINT       DEFAULT NULL,      -- 关联 sys_menu.id
    type        VARCHAR(20)  NOT NULL DEFAULT 'api',  -- menu/button/api
    parent_code VARCHAR(100) DEFAULT '',          -- 树形权限(用于按钮归属菜单)
    sort_order  INT          NOT NULL DEFAULT 0,
    deleted     SMALLINT     NOT NULL DEFAULT 0,
    created_at  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE sys_permission IS '权限项';
COMMENT ON COLUMN sys_permission.code IS '权限编码(如 badge:player:create)';
COMMENT ON COLUMN sys_permission.type IS '类型: menu=菜单 button=按钮 api=接口';
COMMENT ON COLUMN sys_permission.menu_id IS '所属菜单ID';
CREATE INDEX idx_sys_perm_menu ON sys_permission(menu_id);

-- 3. 角色-菜单关联（哪些角色能看到哪些菜单）
CREATE TABLE IF NOT EXISTS sys_role_menu (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_code   VARCHAR(50)  NOT NULL,
    menu_id     BIGINT       NOT NULL,
    created_at  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_role_menu UNIQUE (role_code, menu_id)
);
COMMENT ON TABLE sys_role_menu IS '角色菜单关联';
CREATE INDEX idx_role_menu_role ON sys_role_menu(role_code);

-- 4. 角色-权限关联（保持不变）
CREATE TABLE IF NOT EXISTS sys_role_permission (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_code       VARCHAR(50)  NOT NULL,
    permission_code VARCHAR(100) NOT NULL,
    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_role_perm UNIQUE (role_code, permission_code)
);
COMMENT ON TABLE sys_role_permission IS '角色权限关联';
CREATE INDEX idx_role_perm_role ON sys_role_permission(role_code);

