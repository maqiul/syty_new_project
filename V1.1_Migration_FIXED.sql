    operator_name   VARCHAR(50)  DEFAULT '',
    remark          VARCHAR(200) DEFAULT '',
    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE payment_record IS '支付流水表';
COMMENT ON COLUMN payment_record.tenant_id IS '租户ID';
COMMENT ON COLUMN payment_record.order_id IS '关联订单ID';
COMMENT ON COLUMN payment_record.order_no IS '订单号';
COMMENT ON COLUMN payment_record.player_id IS '客户ID';
COMMENT ON COLUMN payment_record.player_name IS '客户姓名';
COMMENT ON COLUMN payment_record.amount IS '本次支付金额';
COMMENT ON COLUMN payment_record.pay_method IS '支付方式: CASH/WECHAT/ALIPAY/BANK/CREDIT';
COMMENT ON COLUMN payment_record.paid_amount_after IS '支付后该订单累计已付金额';
COMMENT ON COLUMN payment_record.pay_status_after IS '支付后状态';
COMMENT ON COLUMN payment_record.operator_id IS '收款人ID';
COMMENT ON COLUMN payment_record.operator_name IS '收款人姓名';
COMMENT ON COLUMN payment_record.remark IS '备注';
CREATE INDEX idx_payment_order ON payment_record(order_id);
CREATE INDEX idx_payment_player ON payment_record(player_id);
CREATE INDEX idx_payment_method ON payment_record(pay_method);
CREATE INDEX idx_payment_created ON payment_record(created_at);

