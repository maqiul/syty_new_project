-- ----------------------------------------
-- 2.1 stock_log 库存流水表
-- ----------------------------------------
CREATE TABLE IF NOT EXISTS stock_log (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id       BIGINT       NOT NULL,
    shop_id         BIGINT       NOT NULL,
    string_id       BIGINT       NOT NULL,
    change_type     VARCHAR(20)  NOT NULL,
    quantity        INTEGER      NOT NULL,
    before_quantity INTEGER      NOT NULL,
    after_quantity  INTEGER      NOT NULL,
    order_id        BIGINT       DEFAULT NULL,
    order_no        VARCHAR(50)  DEFAULT '',
    operator_id     BIGINT       DEFAULT NULL,
    operator_name   VARCHAR(50)  DEFAULT '',
    remark          VARCHAR(200) DEFAULT '',
    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE stock_log IS '库存流水表';
COMMENT ON COLUMN stock_log.tenant_id IS '租户ID';
COMMENT ON COLUMN stock_log.shop_id IS '店铺ID';
COMMENT ON COLUMN stock_log.string_id IS '线材ID';
COMMENT ON COLUMN stock_log.change_type IS '变动类型: PURCHASE_IN-采购入库 ORDER_OUT-订单扣减 MANUAL_ADJUST-手动调整 RETURN_IN-退货入库 TRANSFER-调拨';
COMMENT ON COLUMN stock_log.quantity IS '变动数量(正数为入库,负数为出库)';
COMMENT ON COLUMN stock_log.before_quantity IS '变动前库存';
COMMENT ON COLUMN stock_log.after_quantity IS '变动后库存';
COMMENT ON COLUMN stock_log.order_id IS '关联订单ID(订单扣减时填写)';
COMMENT ON COLUMN stock_log.order_no IS '关联订单号';
COMMENT ON COLUMN stock_log.operator_id IS '操作人ID';
COMMENT ON COLUMN stock_log.operator_name IS '操作人姓名';
COMMENT ON COLUMN stock_log.remark IS '备注(如采购单号、调整原因)';
CREATE INDEX idx_stock_log_shop ON stock_log(shop_id);
CREATE INDEX idx_stock_log_string ON stock_log(string_id);
CREATE INDEX idx_stock_log_order ON stock_log(order_id);
CREATE INDEX idx_stock_log_created ON stock_log(created_at);

