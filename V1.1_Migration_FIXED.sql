-- ----------------------------------------
-- 4.1 commission_rule 提成规则配置表
-- ----------------------------------------
CREATE TABLE IF NOT EXISTS commission_rule (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id       BIGINT       DEFAULT NULL,
    shop_id         BIGINT       DEFAULT NULL,
    stringer_id     BIGINT       DEFAULT NULL,
    rule_type       VARCHAR(20)  NOT NULL DEFAULT 'FIXED',
    fixed_amount    DECIMAL(10,2) DEFAULT 0,
    percent_rate    DECIMAL(5,2)  DEFAULT 0,
    percent_base    VARCHAR(20)  DEFAULT 'LABOR',
    tier_config     JSONB        DEFAULT NULL,
    effective_from  DATE         NOT NULL DEFAULT CURRENT_DATE,
    effective_to    DATE         DEFAULT NULL,
    enabled         SMALLINT     NOT NULL DEFAULT 1,
    remark          VARCHAR(200) DEFAULT '',
    created_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);
COMMENT ON TABLE commission_rule IS '提成规则配置表';
COMMENT ON COLUMN commission_rule.tenant_id IS '租户ID';
COMMENT ON COLUMN commission_rule.shop_id IS '店铺ID(NULL=全局规则)';
COMMENT ON COLUMN commission_rule.stringer_id IS '穿线师ID(NULL=默认规则)';
COMMENT ON COLUMN commission_rule.rule_type IS '规则类型: FIXED-固定金额 PERCENT-百分比 TIERED-阶梯';
COMMENT ON COLUMN commission_rule.fixed_amount IS '固定提成金额(元/支)';
COMMENT ON COLUMN commission_rule.percent_rate IS '提成比例(%, 如 15.00 表示15%)';
COMMENT ON COLUMN commission_rule.percent_base IS '提成基数: LABOR-手工费 TOTAL-总价 STRING-线材费';
COMMENT ON COLUMN commission_rule.tier_config IS '阶梯配置 JSON';
COMMENT ON COLUMN commission_rule.effective_to IS 'NULL=永久有效';

